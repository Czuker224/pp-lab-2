class Ocena {
    private String przedmiot;
    private double ocena;

    public Ocena(String przedmiot, double ocena) {
        this.przedmiot = przedmiot;
        this.ocena = ocena;
    }

    public String getPrzedmiot() {
        return przedmiot;
    }

    public double getOcena() {
        return ocena;
    }
}